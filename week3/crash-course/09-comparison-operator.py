# Is eqauals?
print("6" == "6")

#Greater than 
print(7 > 6)

#Less than 
print(6 < 7)

#Greater than or equal to 
print(7 >= 7)

#Less than or equal to 
print(6 <= 7)

# AND operation
#Both need to be true for this to evaluate to true
print("6" == "6" and 6 > 8)

#OR operator 
#Only one needs to evaluate to true for this to be true
print(7 > 6 or 8 == 9)
